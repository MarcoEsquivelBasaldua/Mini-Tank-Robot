#ifndef typeDefs_H
#define typeDefs_H

/* unsigned int types */
typedef uint8_t  uint8;
typedef uint16_t uint16;
typedef uint32_t uint32;
typedef uint64_t uint64;

/* signed int types */
typedef int8_t  sint8;
typedef int16_t sint16;
typedef int32_t sint32;
typedef int64_t sint64;

/* float type */
typedef float float32;

#endif